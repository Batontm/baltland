def get_village_info(village_name: str, district: str = None) -> dict:
    """Получает информацию о поселке или возвращает дефолтные значения."""
    # Защита от None
    if not village_name:
        result = DEFAULT_VILLAGE_INFO.copy()
        if district:
            result["district"] = district
        return result
    
    # Пробуем найти точное совпадение
    if village_name in VILLAGES_INFO:
        info = VILLAGES_INFO[village_name].copy()
        if district:
            info["district"] = district
        return info
    
    # Пробуем найти частичное совпадение
    for key, info in VILLAGES_INFO.items():
        if village_name in key or key in village_name:
            result = info.copy()
            if district:
                result["district"] = district
            return result
    
    # Возвращаем дефолтные значения
    result = DEFAULT_VILLAGE_INFO.copy()
    if district:
        result["district"] = district
    return result
