def get_seed(cadastral):
    """–ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç seed –Ω–∞ –æ—Å–Ω–æ–≤–µ –∫–∞–¥–∞—Å—Ç—Ä–æ–≤–æ–≥–æ –Ω–æ–º–µ—Ä–∞ –¥–ª—è –≤–æ—Å–ø—Ä–æ–∏–∑–≤–æ–¥–∏–º–æ—Å—Ç–∏"""
    return int(hashlib.md5(cadastral.encode()).hexdigest()[:8], 16)

def get_area_category(area):
    try:
        area_float = float(area)
        if area_float < 8:
            return 'small'
        elif area_float <= 12:
            return 'medium'
        else:
            return 'large'
    except:
        return 'medium'

def generate_unique_description(row):
    cadastral = str(row['cadastral_number'])
    loc = row['location']
    area = row['area_sotok']
    clean_loc = loc.replace('–ø–æ—Å. ', '').replace('–≥–æ—Ä. ', '')
    
    # –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º seed –¥–ª—è –≤–æ—Å–ø—Ä–æ–∏–∑–≤–æ–¥–∏–º–æ—Å—Ç–∏
    random.seed(get_seed(cadastral))
    
    # –í—ã–±–∏—Ä–∞–µ–º –≤–∞—Ä–∏–∞–Ω—Ç—ã
    intro = random.choice(intro_variants).format(area=area)
    location_line = random.choice(location_variants).format(loc=clean_loc)
    
    desc = f"{intro}\n{location_line}\n\n"
    desc += "üõ† –ö–æ–º–º—É–Ω–∏–∫–∞—Ü–∏–∏:\n"
    
    if loc in gas_confirmed:
        desc += random.choice(electricity_variants_gas) + "\n"
        desc += random.choice(gas_variants_confirmed) + "\n"
    elif loc in coastal:
        desc += random.choice(electricity_variants_standard) + "\n"
        desc += "‚è≥ –ì–∞–∑: –≤ –ø–µ—Ä—Å–ø–µ–∫—Ç–∏–≤–µ/—Ä—è–¥–æ–º üî•\n"
    else:
        desc += random.choice(electricity_variants_standard) + "\n"
        desc += random.choice(gas_variants_perspective) + "\n"
    
    desc += "\nüå≥ –û–ø–∏—Å–∞–Ω–∏–µ:\n"
    
    # –°–ø–µ—Ü–∏—Ñ–∏—á–Ω–æ–µ –æ–ø–∏—Å–∞–Ω–∏–µ –¥–ª—è –∏–∑–≤–µ—Å—Ç–Ω—ã—Ö –ø–æ—Å–µ–ª–∫–æ–≤
    specific_key = None
    for key in location_specific:
        if key in loc:
            specific_key = key
            break
    
    if specific_key:
        desc += random.choice(location_specific[specific_key])
    elif loc in coastal:
        desc += random.choice(coastal_descriptions)
    else:
        desc += random.choice(general_descriptions)
    
    # –î–æ–±–∞–≤–ª—è–µ–º –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–π –ø–æ –ø–ª–æ—â–∞–¥–∏
    area_cat = get_area_category(area)
    desc += "\n\n" + random.choice(area_comments[area_cat])
    
    # –î–æ–±–∞–≤–ª—è–µ–º –∫–∞–¥–∞—Å—Ç—Ä–æ–≤—ã–π –Ω–æ–º–µ—Ä –¥–ª—è —É–Ω–∏–∫–∞–ª—å–Ω–æ—Å—Ç–∏
    desc += f"\n\nüìã –ö–∞–¥–∞—Å—Ç—Ä–æ–≤—ã–π –Ω–æ–º–µ—Ä: {cadastral}"
    
    return desc

# 4. –ü—Ä–∏–º–µ–Ω–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏
