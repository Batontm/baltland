async function main() {
  console.log("Начинаем создание статей блога...")

  for (const article of articles) {
    // Check if slug already exists
    const { data: existing } = await supabase
      .from("news")
      .select("id, slug")
      .eq("slug", article.slug)
      .maybeSingle()

    if (existing) {
      console.log(`⏭  Статья "${article.slug}" уже существует (id: ${existing.id}), пропускаем`)
      continue
    }

    const { data, error } = await supabase
      .from("news")
      .insert({
        title: article.title,
        slug: article.slug,
        meta_title: article.meta_title,
        meta_description: article.meta_description,
        content: article.content,
        sort_order: article.sort_order,
        is_published: true,
        published_at: new Date().toISOString(),
      })
      .select("id, slug")
      .single()

    if (error) {
      console.error(`✗  Ошибка при создании "${article.slug}":`, error.message)
    } else {
      console.log(`✓  Создана статья "${article.slug}" (id: ${data.id})`)
    }
  }

  console.log("\nГотово!")
}

main().catch(console.error)
